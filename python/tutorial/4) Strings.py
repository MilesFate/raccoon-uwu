phrase = 'Giraffe Academy'
# basic print statement
print(phrase)

# This makes the string all upper or lower case
print(phrase.upper())
print(phrase.lower())

# this returns true or false depending on the strings case
print(phrase.upper().isupper())

# this tells how long the string is
print(len(phrase))

# this prints postion 0 in the string
print(phrase[0])

# this tells the index value of a piece in the string
print(phrase.index("G"))

# this replaces
print(phrase.replace("G","Q"))